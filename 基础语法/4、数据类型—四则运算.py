number_01 = int(input("第一个数字: "))
number_02 = int(input("第二个数字: "))

print("%s + %s = %s" % (number_01, number_02, number_01 + number_02))
print("%s - %s = %s" % (number_01, number_02, number_01 - number_02))
print("%s * %s = %s" % (number_01, number_02, number_01 * number_02))
print("%s / %s = %s" % (number_01, number_02, number_01 / number_02))
print("%s %% %s = %s" % (number_01, number_02, number_01 % number_02))



